	<h1 Align="Center">DOA</h1>
	<H2 Align="Center"> SOBRE NÓS: </H2>
		<p><I> Quem Somos Nós?</I></p>
	<p>Olá.</p>

	<p>Somos alunos do Terceiro ano do Instituto Federal do Campus de Capivari, integrado ao Ensino Técnico de Informática e desenvolvemos esse aplicativo com o intuito de Incentivar a Literatura e torná-la mais acessível a todos, a partir da ação de Doar ou Receber um livro.</p>

	<p>E assim nasceu, o DOA.</p>

	<p>Um projeto integrador, que visava fazer uma interface entre a educação tecnológica e a importância da literatura para todos. </p>

	<p> Feito para todos os leitores.</p>

	<H2 Align="Center"> POLÍTICA DE DOAÇÃO: </H2>

	<p> A DOA é um projeto de doação de livros. Sua entrega é feita por envio módico, uma funçao dos correios que auxília no custo, cobrando pelo peso do produto e não pela distância que o objeto irá percorrer, nesse caso, o DOA é disponível para todos os estados do Brasil e reforça a intenção de tornar a literatura acessível para todos. </P>
	<p> Quando um usuário entra em contato com a Equipe Doa e a doação é aprovada, o livro ficará disponível no site até que alguém faça uma solicitação. Em seguida, o doador tem até duas semanas para realizar o envio, caso contrário, o livro será retirado do site por não cumprir com as normas esperadas.</P>

